// Extract CSS custom properties declared in a component's `tailwindCss`
// snippet. We collect top-level `--name: value;` declarations inside
// `@theme { ... }` blocks. Nested `@keyframes` (or any nested block) are
// skipped so `0%`/`100%` selectors don't mis-parse as variables.
//
// Returns `undefined` (not `[]`) when the component has no variables, so
// the optional `cssVariables` field stays absent from the emitted entry
// rather than appearing as an empty array.
export function extractCssVariables(tailwindCss: string | undefined): CssVariableEntry[] | undefined {
  if (!tailwindCss) return undefined;
  const themeBodies: string[] = [];
  let depth = 0;
  // `themeDepth` is the depth at which we entered the current @theme block,
  // or 0 if we aren't inside one. We accumulate characters only when
  // `depth === themeDepth` — i.e. at the top level of @theme, never inside
  // a nested block such as @keyframes.
  let themeDepth = 0;
  let buffer = "";
  let themeBuffer = "";
  for (let i = 0; i < tailwindCss.length; i++) {
    const ch = tailwindCss[i];
    if (ch === "{") {
      const atMatch = /.*@(\w+)\b[^{]*$/.exec(buffer);
      depth++;
      if (themeDepth === 0 && atMatch && atMatch[1] === "theme") {
        themeDepth = depth;
        themeBuffer = "";
      }
      buffer = "";
      continue;
    }
    if (ch === "}") {
      if (themeDepth !== 0 && depth === themeDepth) {
        themeBodies.push(themeBuffer);
        themeBuffer = "";
        themeDepth = 0;
      }
      depth--;
      buffer = "";
      continue;
    }
    buffer += ch;
    if (themeDepth !== 0 && depth === themeDepth) themeBuffer += ch;
  }

  const out: CssVariableEntry[] = [];
  // Matches only lowercase-kebab names to stay in sync with CssVariable.name
  // schema regex /^--[a-z0-9][a-z0-9-]*$/. Uppercase variables are valid CSS
  // but rejected by the schema, so they are silently skipped here rather than
  // extracted and causing a post-build validation failure.
  const varRe = /(--[a-z0-9][a-z0-9-]*)\s*:\s*([^;]+);/g;
  for (const chunk of themeBodies) {
    let m;
    while ((m = varRe.exec(chunk)) !== null) {
      out.push({ name: m[1], defaultValue: m[2].trim() });
    }
  }
  return out.length > 0 ? out : undefined;
}

async function loadRegistryDocsManifest(entries: RegistryEntry[]) {
  if (!(await fs.pathExists(REGISTRY_DOCS_FILE))) {
    throw new Error(`Missing docs metadata source at ${REGISTRY_DOCS_FILE}`);
  }

  const manifest = (await fs.readJson(REGISTRY_DOCS_FILE)) as RegistryDocsManifest;
  if (!manifest.demos?.sourceFile) {
    throw new Error("registry/docs.json must include demos.sourceFile");
  }
  const registrySlugs = entries.map((entry) => entry.name).sort();
  const docsSlugs = manifest.components.map((component) => component.slug).sort();

  if (JSON.stringify(registrySlugs) !== JSON.stringify(docsSlugs)) {
    throw new Error(
      [
        "Registry docs metadata is out of sync with registry/config.ts.",
        `registry/config.ts slugs: ${registrySlugs.join(", ")}`,
        `registry/docs.json slugs: ${docsSlugs.join(", ")}`,
      ].join("\n"),
    );
  }

  return manifest;
}

async function syncDocsRegistryArtifacts(entries: RegistryEntry[], changelogs: Changelogs) {
  await fs.outputJson(APP_PUBLIC_OUTPUT_FILE, entries, { spaces: 2 });
  await fs.emptyDir(APP_PUBLIC_REGISTRY_DIR);
  await fs.outputJson(
    path.join(APP_PUBLIC_REGISTRY_DIR, "index.json"),
    { components: entries.map((entry) => entry.name) },
    { spaces: 2 },
  );

  await Promise.all(
    entries.map((entry) =>
      fs.outputJson(path.join(APP_PUBLIC_REGISTRY_DIR, `${entry.name}.json`), entry, { spaces: 2 }),
    ),
  );

  // Publish the aggregate changelog so future `uniqueui update`/`diff` can
  // resolve per-slug history with a single fetch.
  await fs.outputJson(
    path.join(APP_PUBLIC_REGISTRY_DIR, "changelogs.json"),
    changelogs,
    { spaces: 2 },
  );
}

async function syncDocsUiComponents(entries: RegistryEntry[]) {
  await fs.ensureDir(APP_COMPONENTS_UI_DIR);
  const expectedFiles = new Set(entries.map((entry) => `${entry.name}.tsx`));
  const existingFiles = await fs.readdir(APP_COMPONENTS_UI_DIR);

  await Promise.all(
    existingFiles
      .filter((fileName) => fileName.endsWith(".tsx") && !expectedFiles.has(fileName))
      .map((fileName) => fs.remove(path.join(APP_COMPONENTS_UI_DIR, fileName))),
  );

  await Promise.all(
    entries.map(async (entry) => {
      const componentFile = entry.files.find((file) => file.type === "registry:ui");
      if (!componentFile || typeof componentFile.content !== "string") {
        return;
      }

      await fs.writeFile(path.join(APP_COMPONENTS_UI_DIR, `${entry.name}.tsx`), componentFile.content);
    }),
  );
}

async function syncDocsConfig(manifest: RegistryDocsManifest) {
  await fs.ensureDir(APP_CONFIG_DIR);
  await fs.writeFile(APP_COMPONENTS_CONFIG_FILE, renderComponentsConfig(manifest));
  await fs.writeFile(APP_DOCS_SCENARIOS_CONFIG_FILE, renderDocsScenariosConfig(manifest));
  await fs.writeFile(APP_DEMOS_CONFIG_FILE, await renderDemosConfig(manifest));
}

async function syncShadcnRegistry(entries: RegistryEntry[], manifest: RegistryDocsManifest) {
  // Validate all entries before clearing the directory to avoid partial state on failure.
  for (const entry of entries) {
    if (!entry.files.some((f) => f.type === "registry:ui")) {
      throw new Error(
        `syncShadcnRegistry: component "${entry.name}" has no registry:ui file; cannot emit shadcn registry item.`,
      );
    }
  }

  await fs.emptyDir(APP_PUBLIC_SHADCN_DIR);

  // Build a lookup map from slug → docs metadata
  const docsBySlug = new Map(manifest.components.map((c) => [c.slug, c]));

  // Use Promise.all over entries.map (not concurrent push) so manifest item order matches registry/config.ts.
  const shadcnItems = await Promise.all(
    entries.map(async (entry) => {
      const doc = docsBySlug.get(entry.name);

      // Map each file to the shadcn registry-item format.
      // - registry:ui  → registry:component, installed at components/ui/{slug}.tsx
      // - registry:util → omitted (shadcn projects already have @/lib/utils from `shadcn init`)
      const installPath = `components/ui/${entry.name}.tsx`;
      const shadcnFiles = entry.files
        .filter((f) => f.type === "registry:ui")
        .map((f) => ({
          path: installPath,
          content: f.content,
          type: "registry:component" as const,
          target: installPath,
        }));

      const item: Record<string, unknown> = {
        $schema: "https://ui.shadcn.com/schema/registry-item.json",
        name: entry.name,
        type: "registry:ui",
        ...(doc?.name ? { title: doc.name } : {}),
        ...(doc?.description ? { description: doc.description } : {}),
        dependencies: entry.dependencies,
        files: shadcnFiles,
      };

      // Map tailwindConfig → tailwind.config (shadcn schema key)
      if (entry.tailwindConfig) {
        item.tailwind = { config: entry.tailwindConfig };
      }

      await fs.outputJson(path.join(APP_PUBLIC_SHADCN_DIR, `${entry.name}.json`), item, { spaces: 2 });

      return {
        name: entry.name,
        type: "registry:ui",
        ...(doc?.name ? { title: doc.name } : {}),
        ...(doc?.description ? { description: doc.description } : {}),
        dependencies: entry.dependencies,
        files: shadcnFiles.map(({ content: _content, ...rest }) => rest),
        ...(entry.tailwindConfig ? { tailwind: { config: entry.tailwindConfig } } : {}),
      };
    }),
  );

  await fs.outputJson(
    path.join(APP_PUBLIC_SHADCN_DIR, "registry.json"),
    {
      $schema: "https://ui.shadcn.com/schema/registry.json",
      name: "uniqueui",
      homepage: "https://uniqueui-platform.vercel.app",
      items: shadcnItems,
    },
    { spaces: 2 },
  );
}

async function buildRegistry() {
  console.log("Building registry...");

  const slugs = registry.map((entry) => entry.name);
  const changelogs = await loadChangelogs(slugs);
  const motionMap = await loadMotionMap(slugs);
  const tagsMap = await loadTagsMap(slugs);
  const peerDepsMap = await loadPeerDependenciesMap(slugs);
  const compatibilityMap = await loadCompatibilityMap(slugs);
  const accessibilityMap = await loadAccessibilityMap(slugs);

  const result: RegistryEntry[] = [];

  for (const component of registry) {
    const componentFiles = [];

    for (const file of component.files) {
      if (file.content) {
        componentFiles.push({
          path: file.path,
          content: file.content,
          type: file.type,
        });
        continue;
      }

      const filePath = resolvePathUnderDir(REGISTRY_DIR, file.path);
      try {
        const content = await fs.readFile(filePath, "utf-8");
        componentFiles.push({
          path: file.path,
          content,
          type: file.type,
        });
      } catch (error) {
        console.error(`Error reading file ${file.path} for component ${component.name}:`, error);
        process.exit(1);
      }
    }

    const entryChangelog = changelogs[component.name];
    // Latest entry is first in changelog (newest at top, like a release-notes file).
    const latest = entryChangelog[0];
    const motion = motionMap[component.name];
    const cssVariables = extractCssVariables(component.tailwindCss);
    result.push({
      ...component,
      files: componentFiles,
      meta: { version: latest.version },
      changelog: entryChangelog,
      // Emit peerDependencies only when non-empty; RegistryEntry schema
      // uses .min(1).optional(), which rejects [] but accepts undefined.
      ...(peerDepsMap[component.name].length ? { peerDependencies: peerDepsMap[component.name] } : {}),
      tags: tagsMap[component.name],
      compatibility: compatibilityMap[component.name],
      accessibility: accessibilityMap[component.name],
      ...(motion ? { motion } : {}),
      ...(cssVariables ? { cssVariables } : {}),
    });
  }

  const docsManifest = await loadRegistryDocsManifest(result);

  await fs.outputJson(OUTPUT_FILE, result, { spaces: 2 });
  await syncDocsRegistryArtifacts(result, changelogs);
  await syncDocsUiComponents(result);
  await syncDocsConfig(docsManifest);
  await syncShadcnRegistry(result, docsManifest);
  console.log(`Registry built successfully: ${OUTPUT_FILE}`);
  console.log(`Registry synced to docs public root: ${APP_PUBLIC_OUTPUT_FILE}`);
  console.log(`Registry synced to docs public directory: ${APP_PUBLIC_REGISTRY_DIR}`);
  console.log(`Registry synced to docs ui components: ${APP_COMPONENTS_UI_DIR}`);
  console.log(`Docs config generated from registry source: ${REGISTRY_DOCS_FILE}`);
  console.log(`shadcn-compatible registry synced to: ${APP_PUBLIC_SHADCN_DIR}`);
}

buildRegistry().catch((err) => {
  console.error(err);
  process.exit(1);
});
